package com.github.bluenote


case class Color(r: Float, g: Float, b: Float, a: Float = 1f) {
  def changeAlpha(alphaNew: Float) = Color(r, g, b, alphaNew)
  def toArr = Array(r, g, b, a)
  def toHex = toArr.map(x => (x * 255).toInt)
                   .map(i => f"$i%02X")
                   .mkString
}

