  def hexToColor(_s: String): Color = {
    val s = if (_s.length == 6) _s+"FF" else _s
    assert(s.length == 8)
    val r = Integer.parseInt(s.drop(0).take(2), 16)
    val g = Integer.parseInt(s.drop(2).take(2), 16)
    val b = Integer.parseInt(s.drop(4).take(2), 16)
    val a = Integer.parseInt(s.drop(6).take(2), 16)
    return Color(r.toFloat / 255f, g.toFloat / 255f, b.toFloat / 255f, a.toFloat / 255f)
